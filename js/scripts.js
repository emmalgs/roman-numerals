function romanizer(num) {
    if (num === 1) {
      return "I";
    } else if (num === 5) {
      return "V";
    } else if (num === 10) {
      return num = "X";
    } else if (num === 50) {
      return num = "L";
    } else if (num === 100) {
      return num = "C";
    } else if (num === 500) {
      return num = "D";
    } else if (num === 1000) {
      return num = "M";
    }
}
